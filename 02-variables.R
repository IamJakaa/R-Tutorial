# 02-variables.R

# Numeric / integer variable
x <- 10
y <- 20
sum_xy <- x + y
print(sum_xy)

# Numeric (floating point)

pi_val <- 3.14159
radius <- 2.5
area <- pi_val * radius * radius
print(area)

# Character / string
name <- "Alice"
greeting <- paste("Hello", name)
print(greeting)

#logical/ boolean
is_adult <- TRUE
print(is_adult)